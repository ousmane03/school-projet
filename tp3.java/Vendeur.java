public class Vendeur {
    private int id;
    private double montantVentes;

    // Constructeur de la classe Vendeur
    public Vendeur(int id, double montantVentes) {
        this.id = id;
        this.montantVentes = montantVentes;
    }

    // Getters et setters pour les attributs id et montantVentes
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public double getMontantVentes() {
        return montantVentes;
    }

    public void setMontantVentes(double montantVentes) {
        this.montantVentes = montantVentes;
    }
}

